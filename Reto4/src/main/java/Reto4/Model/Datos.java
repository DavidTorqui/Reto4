package Reto4.Model;

public class Datos {
	
	public  String nombre;
	public  String tipo;
	public  String numerodocumento;
	public  String correo;
	public  String confirmarcorreo;
	public  String pais;
	public  String ciudad;
	public  String numero;
	public  String asunto;
	public  String tiempo;
	public  String letrero;
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getNumerodocumento() {
		return numerodocumento;
	}
	public void setNumerodocumento(String numerodocumento) {
		this.numerodocumento = numerodocumento;
	}
	public String getCorreo() {
		return correo;
	}
	public void setCorreo(String correo) {
		this.correo = correo;
	}
	public String getConfirmarcorreo() {
		return confirmarcorreo;
	}
	public void setConfirmarcorreo(String confirmarcorreo) {
		this.confirmarcorreo = confirmarcorreo;
	}
	public String getPais() {
		return pais;
	}
	public void setPais(String pais) {
		this.pais = pais;
	}
	public String getCiudad() {
		return ciudad;
	}
	public void setCiudad(String ciudad) {
		this.ciudad = ciudad;
	}
	public String getNumero() {
		return numero;
	}
	public void setNumero(String numero) {
		this.numero = numero;
	}
	public String getAsunto() {
		return asunto;
	}
	public void setAsunto(String asunto) {
		this.asunto = asunto;
	}
	public String getTiempo() {
		return tiempo;
	}
	public void setTiempo(String tiempo) {
		this.tiempo = tiempo;
	}
	public String getLetrero() {
		return letrero;
	}
	public void setLetrero(String letrero) {
		this.letrero = letrero;
	}
}
